package ALLPROGRAMS;
import java.util.Scanner;
public class dynamicreder 
{
	public static void main(String[] args) 
	{
		Scanner sc=new Scanner(System.in);
		byte a=sc.nextByte();
		int b=sc.nextInt();
		short c=sc.nextShort();
		double d=sc.nextDouble();
		float e=sc.nextFloat();
		char f=sc.next().charAt(0);
		long g=sc.nextLong();
		boolean h=sc.nextBoolean();
		String i=sc.next();
		System.out.println(a+"byte");
		System.out.println(b+"int");
		System.out.println(c+"short");
		System.out.println(d+"double");
		System.out.println(e+"float");
		System.out.println(f+"char");
		System.out.println(g+"long");
		System.out.println(h+"boolean");
		System.out.println(i+"String");
				}
	}
